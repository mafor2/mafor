      SUBROUTINE stopit_fitaero(txtstring)

! The subroutine writes the message in the variable TXTSTR
! to the message file, closes the message file and stops the program.

      use module_fitaero_io

      implicit none

! Scalar arguments

      character(len=*), intent(in) :: txtstring

! Local variables

      if (fe_log) then
          WRITE (funit_log,*) txtstring
          CLOSE (funit_log)
      endif
      STOP 'FITDIS stopped! Please read the log-file!'

! End of subroutine stopit_fitaero

      end subroutine stopit_fitaero
